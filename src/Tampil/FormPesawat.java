     protected void datatable(){
       Object[] Baris = {"Id_pesawat","Nama","Rute","Tanggal","Jam In","Jam Out","Harga"};
       tabmode = new DefaultTableModel(null,Baris);
       tblJadwal.setModel(tabmode);
       String sql = "select * from tiket";
       try{
           java.sql.Statement stat = conn.createStatement();
           ResultSet hasil = stat.executeQuery(sql);
           while(hasil.next()){
               String id_pesawat = hasil.getString("Id_pesawat");
               String nama = hasil.getString("Nama");
               String rute = hasil.getString("Rute");
               String tanggal = hasil.getString("Tanggal");
               String jam_in = hasil.getString("Jam_In");
               String jam_out = hasil.getString("Jam_Out");
               String harga = hasil.getString("Harga");
               
               
               String[] data = {id_pesawat,nama,rute,tanggal,jam_in,jam_out,harga};
               tabmode.addRow(data);
           }
       }catch(Exception e){
           
       }
   }

