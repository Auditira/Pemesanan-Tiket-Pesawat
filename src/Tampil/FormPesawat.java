    private void btnSimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSimpanMouseClicked
         String sql = "insert into tiket values (?,?,?,?,?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, TxtPesawat.getText());
            stat.setString(2, TxtNama.getText());
            stat.setString(3, TxtRute.getText());
            stat.setString(4, TxtTanggal.getText());
            stat.setString(5, TxtJamIn.getText());
            stat.setString(6, TxtJamOut.getText());
            stat.setString(7, TxtHarga.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "DATA BERHASIL DISIMPAN");
            kosong();
            TxtPesawat.requestFocus();
            datatable();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "DATA GAGAL DISIMPAN"+e);
        }
    }//GEN-LAST:event_btnSimpanMouseClicked

